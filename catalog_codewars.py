def catalog(s, article):
    
    tags=s.split("\n")
    att=[]
    for st in tags:
        if st!="":
            att.append(st)
#     print(att)
    print(article)
    res=[]
    flag=0
    for at in att:
        if article in at:
            flag=1
#             print(at)
            th=(at[at.index('<name>'):at.index('</name>')]).replace('<name>',"")
            pr='prx: $'+(at[at.index('<prx>'):at.index('</prx>')]).replace('<prx>',"")
            qt='qty: '+(at[at.index('<qty>'):at.index('</qty>')]).replace('<qty>',"")
            res.append([th,pr,qt])
    print(res)
    if flag==0:
        return "Nothing"
    print(len(res))
#     res_str="\r\n".join(res)
    res_str=""
    if len(res)==1:
        res_str=f"{res[0][0]} > {res[0][1]} {res[0][2]}"
        print(res_str)
        return res_str
    else:
        for re in range(len(res)-1):
            st=res[re]
#             print(st)
            res_str=res_str+f"{st[0]} > {st[1]} {st[2]}\r\n"
#         print(res_str)
    
        st=res[len(res)-1]
        res_str=res_str+f"{st[0]} > {st[1]} {st[2]}"
        print(res_str)  
        return res_str
